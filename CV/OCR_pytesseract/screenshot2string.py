import pytesseract
from PIL import ImageGrab
import re

# –°–¥–µ–ª–∞—Ç—å —Å–∫—Ä–∏–Ω—à–æ—Ç (–∏–ª–∏ –æ—Ç–∫—Ä—ã—Ç—å –≥–æ—Ç–æ–≤–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ)
